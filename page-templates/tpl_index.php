            <h2 id="micsecs-2019-statistics">MICSECS-2019 Statistics</h2>

            <p>This year, the number of applicants increased by 1.5 times, and as a result 173 applications were submitted, including from 5 far abroad countries.
                Russia was represented by authors from St. Petersburg primarily, Moscow, Nizhny Novgorod, Kirov, Ufa and 4 other regional centers.
                The authors represented 17 universities (ITMO University, St. Petersburg Electrotechnical University “LETI”, Russian State Hydrometeorological University, Moscow State University, Ural Federal University, etc.), 4 research centers, 4 commercial companies and one lyceum.</p>

            <p>All applications were reviewed by an international program committee of 58 scientists and research engineers from Russia, Germany, the USA, Great Britain, Portugal and Finland. They represented ITMO University, St. Petersburg Electrotechnical University “LETI”, Space University of Aerospace Instrumentation, SPIIRAS, Innopolis University, Tampere University, Siberian Federal University, E-Government Center and others.</p>

            <p>459 reviews were prepared (2.65 per submission) and 61% of applications were approved.</p>

